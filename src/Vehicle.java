
public class Vehicle  {

    
   //  the Vehicle  class has  fields
       
    public String start;
    public String speed;
    public String stop;    
    
    // the Vehicle class has two constructor
    
    public Vehicle () {
           	
    }
    public Vehicle (String startStart, String startSpeed ,String startStop ) {
       
    	start = startStart;
        speed = startSpeed;
        stop = startStop;
    }
        
    
        
}